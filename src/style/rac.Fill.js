'use strict';


/**
* The `rac.Fill` function methods and properties for convenience
* `{@link Rac.Fill}` objects.
* @namespace rac.Fill
*/
module.exports = function attachRacFill(rac) {

  /**
  * A `Fill` without color. Removes the fill color when applied.
  * @name none
  * @memberof rac.Fill#
  */
  rac.Fill.none = rac.Fill(null);

} // attachRacFill

